const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
  const arrDec = expr.split(/(.{10})/).filter(O=>O)
  const arrMorse = arrDec.map(val => (val.split(/(.{2})/).filter(O=>O)).map(el => 
    +el === 10 ? '.' : 
    +el === 11 ? '-' :
    el === '**' ? '**' : '')
    .join('')
  )
  return arrMorse.map(val => val === '**********' ? ' ' : MORSE_TABLE[val]).join('')
}

module.exports = {
    decode
}